<h1>Project Update #26: Reds Complete!</h1>

<p>
We have inspected all of the rocket reds this weekend.  They sound awesome and look even awesomer'er!  The blues and whites have also arrived, and we will start the inspections on Monday.  The red, white and blues will all mailed out to y'all via USPS by Friday. 
</p>
<p>
The matte black's production has been delayed in China. Their production will start tomorrow  and we hope to have them completed by Thursday for delivery from the factory via airfreight by the end of the week.  We will post more detail on their expected arrival in our warehouse when we get more info.
</p>
<p>
All in all... We are totally fired up about getting these first-run Crunchers to you!!  They have surpassed our expectations on sound quality,... and overall coolness!
</p>
<p>
Thanks again for all of your patience.  All of you have been really understanding of the complex nature of this endeavor, and we are eager to hear your feedback.
</p>
<p>
One quick note...We are going to be producing a video on how to properly use the CordCrunchers.   I have to emphasize that this is required viewing.  Because this is a completely new technology, there is a learning curve involved in its proper use.   Fortunately the curve is quick, and the storage-cycle will become instinctive after a few uses.   I am really hoping you guys can help us spread the word on the correct way to use the product.  
</p>
<p>
If the demo video falls short, please blast us with thoughtful critiques. This is a very important tool for us as we go forward with this business.
</p>
<p>
Thanks a million to all you backers!!
</p>
<p>
Jay
